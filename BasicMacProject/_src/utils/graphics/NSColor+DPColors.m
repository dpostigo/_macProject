#import "NSColor+DPColors.h"

@implementation NSColor (DPColors)

+ (NSColor *) offwhiteColor {
    return [NSColor colorWithString: @"F2F2F2"];
}


+ (NSColor *) color: (NSString *) string {
    return [NSColor colorWithString: string];
}

@end